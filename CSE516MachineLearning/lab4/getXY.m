function [x, y] = getXY( curr )
    if curr==1
        x=0.5;
        y=1.5;
    end
    if curr==2
        x=1.5;
        y=1.5;
    end
    if curr==3
        x=2.5;
        y=1.5;
    end
    if curr==4
        x=0.5;
        y=0.5;
    end
    if curr==5
        x=1.5;
        y=0.5;
    end
    if curr==6
        x=2.5;
        y=0.5;
    end


end

